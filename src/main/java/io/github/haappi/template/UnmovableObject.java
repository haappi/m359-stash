package io.github.haappi.template;

public class UnmovableObject {
    private final int x;
    private final int y;

    public UnmovableObject(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
